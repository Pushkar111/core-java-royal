package String_Manupulation.java_methods.javaString_Concatenation;

public class Concatenation2 {
	public static void main(String[] args) {
		String fname = "Sachin ";
		String lname = "Tendulkar";
		String fullname = fname.concat(lname);
		System.out.println(fullname);// Sachin Tendulkar
	}
}
