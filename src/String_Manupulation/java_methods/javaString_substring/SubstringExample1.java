package String_Manupulation.java_methods.javaString_substring;

public class SubstringExample1 {
	public static void main(String[] args) {

		String name = "SachinTendulkar";
		System.out.println("Original String: " + name);
		System.out.println("Substring Starting From 6 index: " + name.substring(6));
		System.out.println("Substring Starting From 0 to 6 index: " + name.substring(0, 6));

	}
}
